#include "fractol.h"

int		ft_julia(t_env *env)
{
	int x;
	int y;

	x = 0;
	y = 0;
	while (y < env->y_max)
	{
		while (x < env->x_max)
		{
			
			x++;
		}
		x = 0;
		y++;
	}
}
